/* ============================================================================
   Build the client-side FREE-zone content bundle.
   ----------------------------------------------------------------------------
   Reads netlify/functions/_content/<profil>.js, keeps ONLY the zone:'free'
   blocks, and writes free_content.client.js as:
       window.LP_FREE_CONTENT = { <profil>: { profil, code, nom, accent, blocks } }

   The output contains NO paid content, so it is safe to ship to the browser.
   It is the offline / fallback source for the result page's free profile.

   Regenerate after editing any _content/*.js:  node tools/build-free-content.mjs
   ============================================================================ */
import { writeFileSync } from 'fs';
import { fileURLToPath, pathToFileURL } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const contentDir = join(root, 'netlify', 'functions', '_content');
const profiles = ['miroir', 'bastion', 'cameleon', 'fugitif', 'guetteur', 'sauveur', 'alchimiste', 'incendiaire'];

const out = {};
for (const p of profiles) {
  const mod = await import(pathToFileURL(join(contentDir, p + '.js')).href);
  const c = mod.default || mod.content;
  const freeBlocks = (c.blocks || []).filter((b) => (b.zone || 'free') === 'free');
  out[c.profil] = { profil: c.profil, code: c.code, nom: c.nom, accent: c.accent, blocks: freeBlocks };
  const total = (c.blocks || []).length;
  console.log(`${p}: ${freeBlocks.length}/${total} blocks kept (free)`);
}

const banner =
  '/* AUTO-GENERATED by tools/build-free-content.mjs. Do not edit by hand.\n' +
  '   FREE-zone blocks only (no paid content). Client-side fallback source for\n' +
  '   the result page free profile. Regenerate after editing _content/*.js. */\n';

writeFileSync(join(root, 'free_content.client.js'), banner + 'window.LP_FREE_CONTENT = ' + JSON.stringify(out) + ';\n');
console.log('Wrote free_content.client.js with profiles:', Object.keys(out).join(', '));
