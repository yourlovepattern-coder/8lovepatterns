/* Helper used by the quiz: returns the statement in the active language. */
window.LP_itemText = function (item, lang) {
  if (!item) return '';
  if (lang === 'fr' && window.LP_ITEM_FR && window.LP_ITEM_FR[item.id]) return window.LP_ITEM_FR[item.id];
  return item.text;
};
